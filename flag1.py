import turtle as t;

t.begin_fill();
t.fillcolor('orange');
t.forward(150);
t.left(90);
t.forward(50);
t.right(90);
t.back(150);
t.left(90);
t.back(50);
t.pendown();
t.end_fill();
t.right(90);

t.begin_fill();

t.fillcolor('white');
t.forward(100)
t.left(90)
t.forward(50)
t.left(90)
t.forward(100)
t.left(90)
t.forward(50)
t.left(90)
t.end_fill();


t.showturtle();
t.done();